# 船橋競馬場 2019年の開催日とレース番号から race_id を生成

# 月ごとの開催日リスト（2019年）
months_days = {
    1:  [15, 16, 17, 18],
    2:  [18, 19, 20, 21, 22],
    3:  [11, 12, 13, 14, 15],
    4:  [15, 16, 17, 18, 19],
    5:  [6,  7,  8,  9,  10]
    #,
    #6:  [17, 18, 19, 20, 21],
    #7:  [14, 15, 16, 17],
    #8:  [7,  8,  9,  10, 29, 30, 31],
    #9:  [2,  23, 24, 25, 26, 27],
    #10: [28, 29, 30, 31],
    #11: [1],
    #12: [9,  10, 11, 12, 13],
}

year       = 2019      # 20xx の xx 部分
track_code = 43        # 船橋競馬場のコード

race_id = []
for month, days in months_days.items():
    for day in days:
        for race_no in range(1, 13):  # レース番号 01～12
            race_id.append(
                f"{year}"            # 4桁の年
                f"{track_code:02d}"  # 2桁の競馬場コード
                f"{month:02d}"       # 2桁の月
                f"{day:02d}"         # 2桁の日
                f"{race_no:02d}"     # 2桁のレース番号
            )

print(f"生成された race_id は {len(race_id)} 件です。")
# 動作確認例
print(race_id[:])
# → ['201943011501', '201943011502', '201943011503']
